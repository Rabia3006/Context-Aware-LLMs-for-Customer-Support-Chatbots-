import panel as pn
from typing import List
from langchain_community.llms import Ollama
from langchain.chains import RetrievalQA
from langchain.prompts import PromptTemplate

# Enable Bootstrap theme
pn.extension('bootstrap')

# ========= LLM (Ollama) =========
llm = Ollama(model="llama3")

# ========= Retriever =========
try:
    retriever
except NameError:
    raise RuntimeError("❌ You need to define a retriever before running the app.")

# ========= RAG Chain =========
prompt = PromptTemplate.from_template(
    "Use the following context to answer the question.\n\nContext:\n{context}\n\nQuestion: {question}\nAnswer:"
)

rag_chain = RetrievalQA.from_chain_type(
    llm=llm,
    retriever=retriever,
    chain_type="stuff",
    chain_type_kwargs={"prompt": prompt},
)

# Helper function
def rag_chain_fn(question: str) -> str:
    result = rag_chain.invoke({"query": question})
    return result["result"]

# ========= Widgets =========
query_input = pn.widgets.TextAreaInput(
    name="🔎 Enter your question",
    placeholder="Type your question here…",
    height=120,
    max_length=5000,
    width=400,
)

k_input = pn.widgets.IntSlider(name="📄 Context documents (k)", start=1, end=10, value=2)

run_button = pn.widgets.Button(name="Run", button_type="primary")
clear_button = pn.widgets.Button(name="Clear", button_type="warning")

status_alert = pn.pane.Alert("✅ Ready.", alert_type="success", visible=True, width=600)
answer_md = pn.pane.Markdown("Answer will appear here.")
contexts_md = pn.pane.Markdown("Contexts will appear here.")

# ========= Format Contexts =========
def _format_contexts(texts: List[str]) -> str:
    if not texts:
        return "_No contexts retrieved._"
    blocks = []
    for i, t in enumerate(texts, 1):
        blocks.append(f"**Context {i}:**\n\n```\n{t}\n```")
    return "\n\n---\n\n".join(blocks)

# ========= Button Handlers =========
def on_run_click(event):
    try:
        status_alert.alert_type = "info"
        status_alert.object = "⏳ Running…"

        q = (query_input.value or "").strip()
        if not q:
            status_alert.alert_type = "warning"
            status_alert.object = "⚠️ Please enter a question."
            return

        # Retrieve docs
        k = int(k_input.value)
        docs = retriever.invoke(q) if hasattr(retriever, "invoke") else retriever.get_relevant_documents(q)
        docs = docs[:k] if k and docs else docs
        contexts = [getattr(d, "page_content", str(d)) for d in docs]

        # Show contexts
        contexts_md.object = _format_contexts(contexts)

        # Generate answer
        answer = rag_chain_fn(q)
        answer_md.object = answer

        status_alert.alert_type = "success"
        status_alert.object = "✅ Done."
    except Exception as e:
        status_alert.alert_type = "danger"
        status_alert.object = f"❌ Error: {e}"

def on_clear_click(event):
    query_input.value = ""
    answer_md.object = "Answer will appear here."
    contexts_md.object = "Contexts will appear here."
    status_alert.alert_type = "success"
    status_alert.object = "✅ Cleared."

run_button.on_click(on_run_click)
clear_button.on_click(on_clear_click)

# ========= Bootstrap Template =========
template = pn.template.BootstrapTemplate(
    title="📘 RAG Panel App (Bootstrap Styled)",
    sidebar=[
        pn.pane.Markdown("## Controls"),
        query_input,
        k_input,
        pn.Row(run_button, clear_button),
    ],
    main=[
        status_alert,
        pn.Card(answer_md, title="💡 Answer", collapsible=False, width=600),
        pn.Card(contexts_md, title="📚 Retrieved Contexts", collapsible=True, width=600),
    ]
)

# Show inline in Jupyter/Colab
template.servable()

